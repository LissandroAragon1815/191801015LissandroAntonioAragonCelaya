    private void jButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarActionPerformed
        String nomC = jTextFieldNomC.getText();
        String dir = jTextFieldDirC.getText();
        String nomA = jTextFieldNomA.getText();
        String dirA = jTextFieldDirA.getText();
        String gar = jTextFieldGar.getText();

        String x=jTextFieldPrestamo.getText();
        double prestamo=Double.parseDouble(x);
        double pays = prestamo/10;
        x = jLabelID.getText();
        int id = Integer.parseInt(x);
        int semanas = Integer.parseInt(x);
        Clientes cliente = new Clientes(id, semanas, nomC, dir, nomA, dirA, gar, pays, prestamo);

        try{
            DaoClientes.update(cliente);
            this.crearModelo();
        } catch (SQLException ex){
            Logger.getLogger(Agregar.class.getName()).log(Level.SEVERE, null, ex);
            Logger.getLogger(Pagar.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally{
            jTextFieldDirA.setText("");
            jTextFieldDirC.setText("");
            jTextFieldGar.setText("");
            jTextFieldNomA.setText("");
            jTextFieldNomC.setText("");
            jTextFieldPrestamo.setText("");
            jList1.setModel(modelo);
        }
    }//GEN-LAST:event_jButtonModificarActionPerformed

