    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        if(evt.getClickCount() == 1){
            int index = jList1.getSelectedIndex();
            Clientes cliente = (Clientes)modelo.getElementAt(index);
            jTextFieldNomC.setText(cliente.getNombreCliente());
            jTextFieldDirA.setText(cliente.getDireA());
            jTextFieldDirC.setText(cliente.getDir());
            jTextFieldGar.setText(cliente.getGarantias());
            jTextFieldNomA.setText(cliente.getAval());
            jTextFieldPrestamo.setText(""+cliente.getPrest());
            jLabelID.setText(""+cliente.getId());
        }
    }//GEN-LAST:event_jList1MouseClicked

